
public class ColorSample {
	private float blackVal;
	private float whiteVal;
	public ColorSample(){
		
	}
	public void setBlackVal(float value){
		blackVal = value;
	}
	
	public float getBlackVal(){
		return blackVal;
	}
	
	public void setWhiteVal(float value){
		whiteVal = value;
	}
	
	public float getWhiteVal(){
		return whiteVal;
	}
}
